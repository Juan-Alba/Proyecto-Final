                    -En la actualidad no existe una vacuna para prevenir la enfermedad del coronavirus 2019 (COVID-19).<br>
                    -La mejor forma de prevenir la enfermedad es evitar la exposición a este virus.<br>
                    -Se cree que el virus se propaga principalmente de persona a persona.<br>
                    -Entre las personas que tienen contacto cercano entre ellas (dentro de unos 6 pies de distancia).<br>
                    -Mediante gotitas respiratorias que se producen cuando una persona infectada tose o estornuda.<br>
                    -Estas gotitas pueden llegar a la boca o la nariz de las personas que se encuentren cerca o posiblemente entrar a los pulmones al respirar.
