                    <div class="card-body">
                    <strong>Límpiese las manos con frecuencia</strong><br>
                    -Lávese las manos frecuentemente con agua y jabón por al menos 20 segundos, especialmente después de haber estado en un lugar público, o después de sonarse la nariz, toser o estornudar.<br>
                    -Si no hay agua y jabón fácilmente disponibles, use un desinfectante de manos que contenga al menos un 60 % de alcohol. Cubra todas las superficies de las manos y frótelas hasta que sienta que se secaron.<br>
                    -Evite tocarse los ojos, la nariz y la boca con las manos sin lavar.<br>
                    <br>
                    <strong>Evite el contacto cercano</strong><br>
                    -Evite el contacto cercano con personas que estén enfermas.<br>
                    -Mantenga una distancia entre usted y las otras personas si el COVID-19 se está propagando en su comunidad. Estos es especialmente importante para las personas que tengan un mayor riesgo de enfermarse gravemente.
